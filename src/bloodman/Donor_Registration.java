    public int checkInput()
    {
        String pass1,pass;
        
        pass= pass_passfield.getText();
        pass1= repass_passfield.getText();
        if(!nid_textfield.getText().isEmpty() || !name_textfield.getText().isEmpty() || !pass_passfield.getText().isEmpty() || !repass_passfield.getText().isEmpty()  || !phone_textfield.getText().isEmpty()  || !Day_textfield.getText().isEmpty()  || !month_textfield.getText().isEmpty()  || !year_textfield.getText().isEmpty()) 
        {
            if( pass.equals(pass1))
            {
                return 1;
            }
            else
            {
                return 0;
            }
            
        }
        else 
        {
            return 0;
        }
    }
    
    
