    private void singup_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_singup_buttonActionPerformed
        // TODO add your handling code here:
        int c;
        c= checkInput();
        
        String Inputname= name_textfield.getText();
        String Inputphone= phone_textfield.getText();
        String Inputnid= nid_textfield.getText();
        String Inputpass= pass_passfield.getText();
        String Inputday= Day_textfield.getText();
        String Inputmonth= month_textfield.getText();
        String Inputyear= year_textfield.getText();
        String Inputblood= blood_combo.getSelectedItem().toString();
        String Inputlocation= location_combo.getSelectedItem().toString();
        String InputAvail= "Yes";
        
        if (c==1)
        {
            ResultSet rs =null;
            String str = "Select * from Donor where NID='"+nid_textfield.getText()+"' or Phone='"+phone_textfield.getText()+"'";
            rs=ob.select(str);
            try {
                if (rs.next()) {
                    JOptionPane.showMessageDialog(null,"NID or Phone no. already exits","Error",JOptionPane.ERROR_MESSAGE);//   jodi value alrready thakle tokon
                }
                else
                {
                    String sql = "insert into Donor (D_Name,NID,Blood,Location,Phone,Password,Availability,L_Day,L_Month,L_Year) values('"+Inputname+"','"+Inputnid+"','"+Inputblood+"','"+Inputlocation+"','"+Inputphone+"','"+Inputpass+"','"+InputAvail+"','"+Inputday+"','"+Inputmonth+"','"+Inputyear+"')";
                    ob.insert(sql);
                    JOptionPane.showMessageDialog(null,"Account created\n"+"Please remember these terms and conditions:\n"+"*Must be a regular blood donator.\n"+"*If no blood donation activity in 200 days after last blood donation\n"+" your account will be deleted\n"+"*Any proven foul play will result in account deletion.\n"+"*Always check your account after donation cofirmation to see added record.\n"+"*Set your Availability to yes after 120 days after blood donation.","Success",JOptionPane.INFORMATION_MESSAGE);
                    clear();
                }
        } catch (SQLException ex) {
            Logger.getLogger(Donor_Registration.class.getName()).log(Level.SEVERE, null, ex);
        }
            
        }
             
        
        else
        {
            JOptionPane.showMessageDialog(null,"Invalid Input or Password don't match","Error",JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_singup_buttonActionPerformed

   
