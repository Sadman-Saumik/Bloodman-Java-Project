    private void fsubmit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fsubmit_buttonActionPerformed
        // TODO add your handling code here:
        
        int c;
        c= checkInput();
        
        String Inputname= fname_textfield.getText();
        String Inputphone= fphone_textfield.getText();
        String Inputnid= fnid_textfield.getText();
        String Inputrec= frecord_textfield.getText();
        String Inputdetails= fdetails_textarea.getText();
        String Inputfrom= from_combo.getSelectedItem().toString();
        
        
        if (c==1)
        {
            String sql = "insert into Feedback (Record_no,F_Name,Phone,NID,User_type,Details) values('"+Inputrec+"','"+Inputname+"','"+Inputphone+"','"+Inputnid+"','"+Inputfrom+"','"+Inputdetails+"')";
            ob1.insert(sql);
            JOptionPane.showMessageDialog(null,"Feedback submitted","Success",JOptionPane.INFORMATION_MESSAGE);
            clear();  
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please fill the information","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_fsubmit_buttonActionPerformed

