    private void fphone_textfieldKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_fphone_textfieldKeyTyped
        // TODO add your handling code here:
        char enter = evt.getKeyChar();
        if(!(Character.isDigit(enter))){
            evt.consume();
        }
        
        boolean max = fphone_textfield.getText().length() > 10;
        if ( max ){
        evt.consume();
        }
    }//GEN-LAST:event_fphone_textfieldKeyTyped

