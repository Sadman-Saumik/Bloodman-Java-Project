    public void phone()
    {
        ResultSet rs =null;
        String str = "Select * from Donor where ID ='"+pid_label.getText()+"'";
        rs=ob.select(str);
        try {
            while (rs.next()) {
                pphone_label.setText(rs.getString("Phone"));             
            }
        } catch (SQLException ex) {
            Logger.getLogger(Donor_interface.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
