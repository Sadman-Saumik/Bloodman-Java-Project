    public void tableInput(String str1)
    {
        
        ResultSet rs1 =null;            
        rs1=ob.select(str1);
        
        int id;
        String name = null;
        model1= (DefaultTableModel) pdonor_table.getModel();   
              
        model1.setRowCount(0);
        
        try {
                while (rs1.next())
                {
                    id=rs1.getInt("ID");
                    name=rs1.getString("D_Name");
                    
                    Object[] objs = {id,name};
                    model1.addRow(objs);
                   
                }
            } catch (SQLException ex) {
                Logger.getLogger(Patient_interface.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
